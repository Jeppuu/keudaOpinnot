function monthNames () {
var month = parseInt(document.getElementById('month').value);
  switch (month) {
    case 1:
    alert("Tammikuu");
    break;

    case 2:
    alert("Helmikuu");
    break;

    case 3:
    alert("Maaliskuu");
    break;

    case 4:
    alert("Huhtikuu");
    break;

    case 5:
    alert("Toukokuu");
    break;

    case 6:
    alert("Kesäkuu");
    break;

    case 7:
    alert("Heinäkuu");
    break;

    case 8:
    alert("Elokuu");
    break;

    case 9:
    alert("Syyskuu");
    break;

    case 10:
    alert("Lokakuu");
    break;

    case 11:
    alert("Marraskuu");
    break;

    case 12:
    alert("Joulukuu");
    break;
  }
}
